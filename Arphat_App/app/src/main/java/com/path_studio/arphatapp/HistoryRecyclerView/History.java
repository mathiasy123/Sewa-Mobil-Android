package com.path_studio.arphatapp.HistoryRecyclerView;

public class History {

    private String status;
    private String tanggal;
    private String lokasi_start;
    private String lokasi_end;
    private String harga;


    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getTanggal() {
        return tanggal;
    }

    public void setTanggal(String tanggal) {
        this.tanggal = tanggal;
    }

    public String getLokasi_start() {
        return lokasi_start;
    }

    public void setLokasi_start(String lokasi_start) {
        this.lokasi_start = lokasi_start;
    }

    public String getLokasi_end() {
        return lokasi_end;
    }

    public void setLokasi_end(String lokasi_end) {
        this.lokasi_end = lokasi_end;
    }

    public String getHarga() {
        return harga;
    }

    public void setHarga(String harga) {
        this.harga = harga;
    }
}
